package Entidad;


public class Roles {
    private int id;
    private String Rol;

    public Roles() {
    }

    public Roles(String Rol) {
        this.Rol = Rol;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getRol() {
        return Rol;
    }

    public void setRol(String Rol) {
        this.Rol = Rol;
    }
    
    
}
