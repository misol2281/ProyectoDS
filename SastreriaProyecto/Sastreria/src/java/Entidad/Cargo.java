package Entidad;

public class Cargo {
    int id;
    String Cargo;

    public Cargo() {
    }

    public Cargo(String Cargo) {
        this.Cargo = Cargo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCargo() {
        return Cargo;
    }

    public void setCargo(String Cargo) {
        this.Cargo = Cargo;
    }
    
    
}
